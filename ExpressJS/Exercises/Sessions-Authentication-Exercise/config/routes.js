const restrictedPages = require('./auth');
const homeController = require('../controllers/home');

module.exports = app => {

    //=============================== Home =====================================================//

    app.get('/', homeController.index);

    //=============================== About ====================================================//

    app.get('/about');

    //=============================== Register =================================================//

    app.get('/register');
    app.post('/register');

    //=============================== Logout ===================================================//

    app.post('/logout');

    //=============================== Login ====================================================//

    app.get('/login');
    app.post('/login');

    //=============================== Add Car ==================================================//

    app.get('/cars/add');
    app.post('/cars/add');

    //=============================== All Cars ==================================================//

    app.get('/cars/all');

    //=============================== Get User By Id =============================================//

    app.get('/users/:id');

    //=============================== Cars Rented By Id ===========================================//

    app.get('/cars/rent/:id');
    app.post('/cars/rent/:id');

    //=============================== Everything else =============================================//

    app.all('*', (req, res) => {
        res.status(404);
        res.send('404 Not Found');
        res.end();
    });
};